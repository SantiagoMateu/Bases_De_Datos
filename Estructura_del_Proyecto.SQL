CREATER DATABASE PROYECTO;

USE PROYECTO;

CREATE TABLE PRODUCTOS(
    PRO_ID int auto_increment,
    PRO_NOMBRES Varchar (100) not null,
    PRO_MAR_ID int,
    PRO_UDM_ID int,
    PRO_PES_ID int,
    PRO_VALOR decimal (6) not null
    Primary key (PRO_ID)
    Foreign key (PRO_MAR_ID) References MARCAS (MAR_ID)
    Foreign key (PRO_UDM_ID) References UDM (UDM_ID)
    Foreign key (PRO_PES_ID) References PESOS (PES_ID)
);

CREATE TABLE STOCKS(
STO_ID int auto_increment,
STO_PRO_ID int,
STO_SDS_ID int,
STO_CANTIDAD decimal (10) not null
Primary key (STO_ID)
Foreign key (STO_PRO_ID) References PRODUCTOS (PRO_ID)
Foreign key (STO_SDS_ID) References SDS (SDS_ID)
);

CREATE TABLE MARCAS(
MAR_ID int auto_increment,
MAR_NOMBRES Varchar (10)
Primary key (MAR_ID)
);

CREATE TABLE PESOS(
    PES_ID int auto_increment,
    PES_NUMEROS DECIMAL (10,2) not null,
    Primary key (PES_ID)
);

CREATE TABLE UNIDADES_DE_MEDIDA(
UDM_ID int auto_increment,
UDM_NOMBRES Varchar (10) not null
Primary key (UDM_ID)
);

CREATE TABLE SUCURSALES_DE_SUPERMERCADOS(
SDS_ID int auto_increment,
SDS_NOMBRES Varchar (10) not null
);

